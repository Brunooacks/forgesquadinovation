class ForgeSquadPipeline:
    """Orchestrates the ForgeSquad multi-agent pipeline using AutoGen GroupChat."""

    def __init__(self, auto_approve: bool = False):
        self.agents = create_all_agents()
        self.approval_gate = ApprovalGate(auto_approve=auto_approve)
        self.run_id = datetime.now(timezone.utc).strftime("%Y%m%d%H%M%S")
        self.audit_trail: list[dict[str, Any]] = []
        self.accumulated_context: dict[str, str] = {}

    async def execute(
        self,
        project_name: str,
        project_description: str,
        skip_phases: list[int] | None = None,
    ) -> dict[str, Any]:
        """Executes the full ForgeSquad pipeline."""

        logger.info("Pipeline %s started for project: %s", self.run_id, project_name)
        self.accumulated_context["ProjectName"] = project_name
        self.accumulated_context["ProjectDescription"] = project_description

        self._log_event("PipelineStarted", "N/A", "Pipeline",
                        f"Started for project: {project_name}")

        results: list[dict[str, Any]] = []

        for phase in PIPELINE_PHASES:
            if skip_phases and phase["number"] in skip_phases:
                logger.info("Skipping phase %d: %s", phase["number"], phase["name"])
                continue

            phase_result = await self._execute_phase(phase)
            results.append(phase_result)

            if phase_result.get("status") == "rejected":
                logger.warning("Pipeline paused at phase %s", phase["name"])
                return {
                    "run_id": self.run_id,
                    "status": "paused",
                    "paused_at": phase["name"],
                    "phases": results,
                    "audit_trail": self.audit_trail,
                }

        self._log_event("PipelineCompleted", "N/A", "Pipeline", "Completed successfully")

        return {
            "run_id": self.run_id,
            "status": "completed",
            "phases": results,
            "audit_trail": self.audit_trail,
        }

    async def _execute_phase(self, phase: dict) -> dict[str, Any]:
        """Executes a single pipeline phase using SelectorGroupChat."""

        phase_name = phase["name"]
        phase_number = phase["number"]
        logger.info("Starting phase %d: %s", phase_number, phase_name)

        self._log_event("PhaseStarted", phase_name,
                        ", ".join(phase["agents"]),
                        f"Phase {phase_number}: {phase_name}")

        # Select agents for this phase
        phase_agents = [self.agents[a] for a in phase["agents"] if a in self.agents]

        if not phase_agents:
            raise ValueError(f"No agents found for phase {phase_name}")

        # Build initial message with context
        initial_message = self._build_phase_prompt(phase)

        # Create group chat with selector (LLM-based speaker selection)
        termination = MaxMessageTermination(max_messages=phase.get("max_rounds", 10))

        team = SelectorGroupChat(
            participants=phase_agents,
            model_client=create_standard_client(),  # Selector uses cheaper model
            termination_condition=termination,
            selector_prompt=(
                "You are managing a software engineering squad discussion. "
                "Based on the conversation so far, select the next speaker. "
                f"Available speakers: {', '.join(phase['agents'])}. "
                "Follow this order for the current phase steps: "
                f"{', '.join(s['agent'] for s in phase['steps'])}. "
                "Return ONLY the speaker name."
            ),
        )

        # Run the group chat
        output_messages: list[str] = []
        async for message in team.run_stream(task=initial_message):
            if hasattr(message, "content") and message.content:
                output_messages.append(f"**{getattr(message, 'source', 'System')}:** {message.content}")
                logger.debug("[%s] %s: %s",
                             phase_name,
                             getattr(message, "source", "?"),
                             message.content[:100])

        phase_output = "\n\n".join(output_messages)
        output_hash = hashlib.sha256(phase_output.encode()).hexdigest()

        # Store in accumulated context
        self.accumulated_context[f"{phase_name}_output"] = phase_output

        self._log_event("PhaseExecuted", phase_name, "GroupChat",
                        f"Phase completed with {len(output_messages)} messages",
                        artifact_hash=output_hash)

        # Handle checkpoint
        checkpoint = phase.get("checkpoint", {})
        if checkpoint.get("enabled"):
            cp_number = checkpoint["number"]
            logger.info("Checkpoint %d reached", cp_number)

            approval = await self.approval_gate.request_approval(
                ApprovalRequest(
                    run_id=self.run_id,
                    checkpoint_number=cp_number,
                    phase_name=phase_name,
                    artifact_summary=phase_output[:2000],
                    artifact_hash=output_hash,
                )
            )

            self._log_event(
                "CheckpointDecided", phase_name, approval.decided_by,
                f"Checkpoint {cp_number}: {approval.decision}",
            )

            if approval.decision == "rejected":
                return {
                    "phase": phase_name,
                    "number": phase_number,
                    "status": "rejected",
                    "checkpoint": cp_number,
                    "output": phase_output,
                }

        return {
            "phase": phase_name,
            "number": phase_number,
            "status": "completed",
            "output": phase_output,
            "output_hash": output_hash,
            "messages": len(output_messages),
        }

    def _build_phase_prompt(self, phase: dict) -> str:
        """Builds the initial prompt for a phase with accumulated context."""

        prompt = f"## Phase {phase['number']}: {phase['name']}\n\n"
        prompt += "### Steps to execute:\n"
        for i, step in enumerate(phase["steps"], 1):
            prompt += f"{i}. **{step['name']}** (lead: {step['agent']})\n"

        prompt += f"\n### Project: {self.accumulated_context.get('ProjectName', 'Unknown')}\n"
        prompt += f"### Description: {self.accumulated_context.get('ProjectDescription', 'N/A')}\n"

        # Add relevant context from previous phases
        for key, value in self.accumulated_context.items():
            if key.endswith("_output") and len(value) > 0:
                phase_label = key.replace("_output", "")
                prompt += f"\n### Context from {phase_label}:\n"
                prompt += value[:1500] + ("\n... (truncated)" if len(value) > 1500 else "")
                prompt += "\n"

        prompt += "\nPlease collaborate to complete all steps for this phase. "
        prompt += "Each agent should contribute according to their expertise.\n"

        return prompt

    def _log_event(
        self,
        event_type: str,
        phase: str,
        agent: str,
        message: str,
        artifact_hash: str | None = None,
    ) -> None:
        """Logs an audit event."""
        event = {
            "run_id": self.run_id,
            "event_type": event_type,
            "phase": phase,
            "agent": agent,
            "message": message,
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "artifact_hash": artifact_hash,
        }
        # Chain hash for integrity
        prev_hash = self.audit_trail[-1].get("event_hash", "genesis") if self.audit_trail else "genesis"
        content = f"{event['run_id']}|{event_type}|{phase}|{agent}|{message}|{prev_hash}"
        event["event_hash"] = hashlib.sha256(content.encode()).hexdigest()
        event["previous_hash"] = prev_hash

        self.audit_trail.append(event)
        logger.info("[AUDIT] %s | %s | %s | %s", event_type, phase, agent, message)
