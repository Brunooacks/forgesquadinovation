async def generate_c4_diagram(description: str, level: str = "container") -> str:
    """Generates a C4 Model diagram from a system description.

    Args:
        description: Description of the system to diagram.
        level: C4 level — context, container, component, or code.

    Returns:
        Mermaid diagram code for the requested C4 level.
    """
    templates = {
        "context": """
C4Context
    title System Context Diagram
    Person(user, "User", "System user")
    System(system, "Target System", "System under design")
    System_Ext(ext1, "External System 1", "Integration point")
    Rel(user, system, "Uses")
    Rel(system, ext1, "Integrates with")
""",
        "container": """
C4Container
    title Container Diagram
    Person(user, "User")
    Container_Boundary(system, "System") {
        Container(web, "Web App", "React", "Frontend SPA")
        Container(api, "API", ".NET 8", "REST API")
        Container(worker, "Worker", ".NET 8", "Background processing")
        ContainerDb(db, "Database", "PostgreSQL", "Primary store")
        ContainerDb(cache, "Cache", "Redis", "Caching layer")
    }
    Rel(user, web, "Uses", "HTTPS")
    Rel(web, api, "Calls", "HTTPS/JSON")
    Rel(api, db, "Reads/Writes")
    Rel(api, cache, "Reads/Writes")
""",
    }
    diagram = templates.get(level, templates["container"])
    return f"## C4 Diagram — {level} level\n\n```mermaid\n{diagram}\n```\n"


async def create_adr(title: str, context: str, decision: str, consequences: str) -> str:
    """Creates an Architecture Decision Record (ADR).

    Args:
        title: Title of the architectural decision.
        context: Context explaining why this decision is needed.
        decision: The chosen option and justification.
        consequences: Positive and negative consequences.

    Returns:
        Formatted ADR document in Markdown.
    """
    adr_number = datetime.now(timezone.utc).strftime("%Y%m%d%H%M")
    return f"""# ADR-{adr_number}: {title}

**Date:** {datetime.now(timezone.utc).strftime("%Y-%m-%d")}
**Status:** Proposed

## Context
{context}

## Decision
{decision}

## Consequences
{consequences}

---
*Generated by ForgeSquad Architect Agent*
"""


async def validate_compliance(
    artifact: str,
    regulation: str = "all",
) -> str:
    """Validates an artifact against financial regulations.

    Args:
        artifact: Description of the artifact to validate.
        regulation: Regulation to check — bacen_4893, lgpd, bian, or all.

    Returns:
        Compliance validation report.
    """
    checks = {
        "bacen_4893": [
            ("Cybersecurity Policy", "Pending", "High"),
            ("Incident Response Plan", "Pending", "Critical"),
            ("Periodic Risk Assessment", "Pending", "High"),
        ],
        "lgpd": [
            ("Legal Basis Defined", "Pending", "Critical"),
            ("Data Subject Rights", "Pending", "High"),
            ("Breach Notification Process", "Pending", "High"),
            ("DPIA Assessment", "Pending", "Medium"),
        ],
        "bian": [
            ("Service Domain Mapping", "Pending", "Medium"),
            ("Service Operations Alignment", "Pending", "Medium"),
        ],
    }

    report = "## Compliance Validation Report\n\n"
    report += f"**Date:** {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M')}\n"
    report += f"**Regulation:** {regulation}\n\n"
    report += "| Requirement | Status | Risk |\n|---|---|---|\n"

    selected = checks if regulation == "all" else {regulation: checks.get(regulation, [])}
    for reg, items in selected.items():
        for req, status, risk in items:
            report += f"| [{reg}] {req} | {status} | {risk} |\n"

    return report


async def check_tech_radar(technology: str) -> str:
    """Checks if a technology is approved in the company Tech Radar.

    Args:
        technology: Name of the technology to check.

    Returns:
        Tech Radar status and recommendation.
    """
    radar = {
        "C#": ("Adopt", "Primary backend language"),
        "PostgreSQL": ("Adopt", "Primary relational database"),
        "React": ("Adopt", "Primary frontend framework"),
        "Kafka": ("Adopt", "Event streaming platform"),
        "Redis": ("Adopt", "Caching layer"),
        "MongoDB": ("Trial", "Document-oriented workloads"),
        "GraphQL": ("Trial", "BFF scenarios"),
        "Angular": ("Hold", "Legacy support only"),
    }
    if technology in radar:
        ring, notes = radar[technology]
        return f"**{technology}** — Ring: {ring} | Notes: {notes}"
    return f"**{technology}** — Not in Tech Radar. Requires Architecture Board approval."


